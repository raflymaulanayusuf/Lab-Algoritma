#include <iostream>
using namespace std;

int main () {
	int a = 10, b = 5;
	
	cout << "Nilai A adalah " << a << endl;
	cout << "Nilai ++A adalah " << ++a << endl;
	cout << "Nilai B adalah" << b << endl;
	cout << "Nilai B-- adalah " << b << endl;
	cout << "Nilai B adalah " << b << endl;
	cout << "Nilai ++B adalah " << ++b << endl;
	cout << "Nilai A + 5 adalah " << a+5 << endl;
	cout << "Nilai 5 + A adalah " << 5+a << endl;
	cout << "Nilai a fix nya adalah " << a;
	
	return 0;
}
